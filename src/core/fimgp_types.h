template <typename T,uint N> constexpr uint aligment()
{
	return 	constexpr_is_numeric<T>()?(constexpr_min<sizeof(T)*((N%2U)==0U?constexpr_highest_pow2_divisor<N>(1):1U),16U>()):sizeof(T);
}
